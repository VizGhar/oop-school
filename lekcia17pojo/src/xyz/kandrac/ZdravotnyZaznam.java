public class ZdravotnyZaznam {

    String menoPacienta;
    double tlak;
    double tep;
    int vyska;
    double hmotnost;

    public String getMenoPacienta() {
        return menoPacienta;
    }

    public void setMenoPacienta(String menoPacienta) {
        this.menoPacienta = menoPacienta;
    }

    public double getTlak() {
        return tlak;
    }

    public void setTlak(double tlak) {
        this.tlak = tlak;
    }

    public double getTep() {
        return tep;
    }

    public void setTep(double tep) {
        this.tep = tep;
    }

    public int getVyska() {
        return vyska;
    }

    public void setVyska(int vyska) {
        this.vyska = vyska;
    }

    public double getHmotnost() {
        return hmotnost;
    }

    public void setHmotnost(double hmotnost) {
        this.hmotnost = hmotnost;
    }
}
